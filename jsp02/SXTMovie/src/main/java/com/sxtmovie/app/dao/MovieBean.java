package com.sxtmovie.app.dao;

public class MovieBean {
	
	private int movieRank;
	private String movieTitle;
	private String movieImgsrc;
	private String movieRvRate;
	private String movieGeRate;
	private String movieOpDate;
	private String movieDday;
	private String movieAge;
	private String movieHref;

	public int getMovieRank() {
		return movieRank;
	}
	public void setMovieRank(int movieRank) {
		this.movieRank = movieRank;
	}
	public String getMovieTitle() {
		return movieTitle;
	}
	public void setMovieTitle(String movieTitle) {
		this.movieTitle = movieTitle;
	}
	public String getMovieImgsrc() {
		return movieImgsrc;
	}
	public void setMovieImgsrc(String movieImgsrc) {
		this.movieImgsrc = movieImgsrc;
	}
	public String getMovieRvRate() {
		return movieRvRate;
	}
	public void setMovieRvRate(String movieRvRate) {
		this.movieRvRate = movieRvRate;
	}
	public String getMovieGeRate() {
		return movieGeRate;
	}
	public void setMovieGeRate(String movieGeRate) {
		this.movieGeRate = movieGeRate;
	}
	public String getMovieOpDate() {
		return movieOpDate;
	}
	public void setMovieOpDate(String movieOpDate) {
		this.movieOpDate = movieOpDate;
	}
	public String getMovieDday() {
		return movieDday;
	}
	public void setMovieDday(String movieDday) {
		this.movieDday = movieDday;
	}
	public String getMovieAge() {
		return movieAge;
	}
	public void setMovieAge(String movieAge) {
		this.movieAge = movieAge;
	}
	public String getMovieHref() {
		return movieHref;
	}
	public void setMovieHref(String movieHref) {
		this.movieHref = movieHref;
	}
	
}
